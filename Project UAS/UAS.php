                    <h3>Siapa iKON?</h3>
                    <p>iKON (Hangul: 아이콘) iKON atau ||¦||¦¦ (dalam bentuk logo), adalah grup idola pria asal korea selatan yang dibentuk
                         pada tahun 2015 oleh YG Entertainment dan beranggotakan enam orang. Grup ini pertama kali diperkenalkan dalam acara 
                         realitas WIN: Who is Next sebagai "Tim B". Setelah itu, Tim B muncul kembali dalam acara realitas survival berjudul 
                         Mix & Match, yang menentukan tujuh anggota formasi akhir dari iKON: B.I, Bobby, Jay, Ju-ne, Song, DK dan Chan.</p>
                    <p>Grup ini merilis album studio debut mereka berjudul Welcome Back (2015), yang menduduki posisi teratas di Gaon Album 
                        Chart Korea Selatan dan menghasilkan singel-singel nomor satu seperti "My Type", "Apology" dan "Dumb & Dumber" dan 
                        tiga singel yang menduduki posisi 10 teratas: "Rhythm Ta", "Airplane" dan "Anthem". Album ini termasuk sukses secara 
                        komersial, terjual sebanyak 260.000 kopi di Asia, sementara lagunya terjual sebanyak 4,8 juta kopi, yang mengantarkan 
                        mereka untuk memenangkan sejumlah penghargaan artis pendatang baru terbaik pada acara penghargaan utama Asia, termasuk 
                        Mnet Asian Music Awards, Japan Record Awards dan QQ Music Awards. Dua tahun berikutnya, mereka merilis singel "#WYD" dan 
                        "New Kids: Begin", dan berfokus pada tur Asia dan beberapa tur konser di Jepang. Album studio kedua mereka, yang bertajuk 
                        Return, dirilis pada tanggal 25 Januari 2018.</p>
